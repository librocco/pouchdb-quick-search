import crypto from 'crypto';
import md5 from 'md5-jkmyers';

export const MD5 = function (string) {
  /* istanbul ignore if */
  if (process.browser) {
    return md5(string);
  }
  return crypto.createHash('md5').update(string).digest('hex');
};

